const Calculadora = {
    soma: function(a,b){
        return a + b;
    },
    subtrair: function(a,b){
        return a - b;
    },
    dividir: function(a,b){
        return a / b;
    },
    multiplicar: function(a,b){
        return a * b;
    }

}

console.log(Calculadora.soma(5,5));
console.log(Calculadora.subtrair(5,6));
console.log(Calculadora.dividir(10,2));
console.log(Calculadora.multiplicar(5,5));















